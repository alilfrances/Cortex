from __future__ import annotations

import cortex.tokenizer as tokenizer_mod
import evals.run_evals as eval_mod
from evals.perf_ingest import run_perf
from evals.run_evals import (
    GoldTask,
    _format_markdown,
    _precision_recall,
    _symbol_hit,
)


def test_precision_recall_scores_expected_file_overlap():
    precision, recall = _precision_recall({"a.py", "b.py"}, {"b.py", "c.py"})

    assert precision == 0.5
    assert recall == 0.5


def test_symbol_hit_accepts_qualname_leaf_in_matching_file():
    items = [
        {
            "path": "app/service.py",
            "content": "class AuthService:\n    def login(self):\n        pass\n",
        }
    ]

    assert _symbol_hit(items, "app/service.py:AuthService.login")
    assert not _symbol_hit(items, "app/service.py:AuthService.logout")


def test_format_markdown_contains_aggregate_table():
    task = GoldTask(
        repo="python_app",
        description="Find auth login flow",
        expected_files=("app/auth.py",),
        expected_symbols=("app/auth.py:AuthService.login",),
        budget=800,
    )
    rows = [
        {
            "task": task,
            "mode": "pagerank",
            "precision": 1.0,
            "precision_at_3": 1 / 3,
            "recall": 1.0,
            "file_precision": 1.0,
            "file_recall": 1.0,
            "symbol_recall": 1.0,
            "tokens": 120,
            "latency_ms": 3.4,
            "files": ["app/auth.py"],
        }
    ]

    markdown = _format_markdown(rows)

    assert "Generated by `python3 evals/run_evals.py`." in markdown
    assert "| Mode | Tasks | Precision | Precision@3 | Recall | Avg Tokens | Avg Latency ms |" in markdown
    assert "| pagerank | 1 | 1.000 | 0.333 | 1.000 | 120 | 3.4 |" in markdown
    assert "--stdlib-tokens" in _format_markdown(
        rows,
        command="python3 evals/run_evals.py --stdlib-tokens",
    )


def test_stdlib_eval_override_restores_optional_tokenizer_state(tmp_path, monkeypatch):
    sentinel_encoding = object()
    monkeypatch.setattr(tokenizer_mod, "_tiktoken_encoding", sentinel_encoding)
    monkeypatch.setattr(tokenizer_mod, "_tiktoken_unavailable", False)
    monkeypatch.setattr(tokenizer_mod, "_force_stdlib_segments", False)
    monkeypatch.setattr(eval_mod, "GOLD_TASKS", ())
    monkeypatch.setattr(eval_mod, "build_fixture_repos", lambda _base: {})

    markdown, rows = eval_mod.run_evals(
        tmp_path / "results.md",
        stdlib_tokens=True,
    )

    assert rows == []
    assert "--stdlib-tokens" in markdown
    assert tokenizer_mod._tiktoken_encoding is sentinel_encoding
    assert tokenizer_mod._tiktoken_unavailable is False
    assert tokenizer_mod._force_stdlib_segments is False


def test_incremental_perf_harness_counts_only_changed_file_reads():
    result = run_perf(file_count=25, max_seconds=5.0)

    assert result["passed"] is True
    assert result["content_reads"] == result["expected_content_reads"]
